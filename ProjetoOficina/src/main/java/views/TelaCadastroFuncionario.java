    private void telefoneFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_telefoneFieldActionPerformed
String telefone = telefoneField.getText();

    // Verifica se o telefone tem pelo menos 10 dígitos (para números no Brasil)
    if (telefone.matches("\\d{10,11}")) {
        System.out.println("Telefone capturado: " + telefone);
    } else {
        JOptionPane.showMessageDialog(this, "Por favor, insira um telefone válido com 10 ou 11 dígitos.", "Erro", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_telefoneFieldActionPerformed

    private void emailFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_emailFieldActionPerformed
       String email = emailField.getText();

    // Valida o formato do e-mail com regex
    if (email.matches("^[\\w-.]+@[\\w-]+\\.[a-z]{2,}$")) {
        System.out.println("E-mail capturado: " + email);
    } else {
        JOptionPane.showMessageDialog(this, "Por favor, insira um e-mail válido.", "Erro", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_emailFieldActionPerformed

    private void salvarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarButtonActionPerformed
                                      
    String nome = nomeField.getText();
    String cargo = cargoField.getText();
    String telefone = telefoneField.getText();
    String email = emailField.getText();
    String salarioTexto = salarioField.getText();

    if (nome.isEmpty() || cargo.isEmpty() || telefone.isEmpty() || email.isEmpty() || salarioTexto.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, preencha todos os campos.", "Erro", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        double salario = Double.parseDouble(salarioTexto);

        if (salario <= 0) {
            JOptionPane.showMessageDialog(this, "O salário deve ser maior que zero.", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Aqui você pode criar o objeto Funcionario e salvar no banco
        Funcionario funcionario = new Funcionario(0, nome, cargo, telefone, email, salario);
        FuncionarioDAO dao = new FuncionarioDAO();
        dao.adicionarFuncionario(funcionario);

        JOptionPane.showMessageDialog(this, "Funcionário salvo com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        limpar(); // Método para limpar os campos após salvar
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "O campo salário deve ser um número válido.", "Erro", JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_salvarButtonActionPerformed

