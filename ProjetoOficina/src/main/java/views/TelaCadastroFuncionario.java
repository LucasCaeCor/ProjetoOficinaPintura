    private void salarioFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salarioFieldActionPerformed
String salarioTexto = salarioField.getText();
    try {
        // Converte o texto para um número decimal
        double salario = Double.parseDouble(salarioTexto);

        if (salario <= 0) {
            JOptionPane.showMessageDialog(this, "O salário deve ser maior que zero.", "Erro", JOptionPane.ERROR_MESSAGE);
        } else {
            System.out.println("Salário digitado: " + salario);
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Por favor, insira um valor numérico válido para o salário.", "Erro", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_salarioFieldActionPerformed


