     public void preencherTabela() {
        DefaultTableModel dtm = (DefaultTableModel) servicosTable.getModel();
        dtm.setRowCount(0); // Limpa a tabela antes de adicionar os novos dados
        
         ServicoDAO dao = new ServicoDAO();
        List<Servico> servicos = dao.listarServicos(); // Retorna a lista de serviços do banco
        
        for (Servico servico : servicos) {
            dtm.addRow(new Object[] {
                servico.getId(),
                servico.getNome(),
                servico.getDescricao(),
                servico.getPreco(),
                servico.getFuncionariosId()
                
            });
        }
    }

    
 

