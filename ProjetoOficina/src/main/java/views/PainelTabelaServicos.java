    private void editarServicoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarServicoButtonActionPerformed
   // Recuperar a linha selecionada na tabela
    int selectedRow = servicosTable.getSelectedRow();
    if (selectedRow != -1) {
        // Obtém os dados da linha selecionada
        int idServico = (int) servicosTable.getValueAt(selectedRow, 0); // ID
        String nomeServico = (String) servicosTable.getValueAt(selectedRow, 1); // Nome
        String descricaoServico = (String) servicosTable.getValueAt(selectedRow, 2); // Descrição
        double precoServico = (double) servicosTable.getValueAt(selectedRow, 3); // Preço4
        int funcionariosId  = (int) servicosTable.getValueAt(selectedRow, 4); // Preço4


        // Exibe caixas de diálogo para editar os dados
        String novoNomeServico = JOptionPane.showInputDialog(this, "Editar Nome:", nomeServico);
        String novaDescricao = JOptionPane.showInputDialog(this, "Editar descrição:", descricaoServico);
        String novoPreco = JOptionPane.showInputDialog(this, "Editar preço:", precoServico);
        String novofuncionariosId = JOptionPane.showInputDialog(this, "Editar funcionario:", funcionariosId);

        // Verifica se os dados foram preenchidos
        if (novaDescricao != null && novoPreco != null && novoNomeServico != null && novofuncionariosId != null) {
            try {
                double preco = Double.parseDouble(novoPreco); // Converte o preço para Double
                int funcionario = Integer.parseInt(novofuncionariosId);
                // Cria um objeto Servico com os novos valores
                Servico servicoEditado = new Servico(idServico, novoNomeServico, novaDescricao, preco, funcionario);
                ServicoDAO dao = new ServicoDAO();

                // Atualiza no banco de dados
                boolean sucesso = dao.editarServico(servicoEditado);
                if (sucesso) {
                    preencherTabela(); // Atualiza a tabela
                    JOptionPane.showMessageDialog(this, "Serviço atualizado com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao atualizar o serviço.");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Erro ao converter preço.");
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Selecione um serviço para editar.");
    }

    }//GEN-LAST:event_editarServicoButtonActionPerformed

    private void excluirServicoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirServicoButtonActionPerformed
// Supondo que você tenha uma tabela para selecionar o serviço a ser excluído
int selectedRow = servicosTable.getSelectedRow();
if (selectedRow == -1) {
    JOptionPane.showMessageDialog(this, "Selecione um serviço para excluir.", "Aviso", JOptionPane.WARNING_MESSAGE);
    return;
}

// Obtendo os dados do serviço selecionado
int id = (int) servicosTable.getValueAt(selectedRow, 0); // ID
String nomeServico = (String) servicosTable.getValueAt(selectedRow, 1); // Nome

// Confirmando a exclusão
int confirm = JOptionPane.showConfirmDialog(this, 
    "Tem certeza que deseja excluir o serviço: " + nomeServico + "?", 
    "Confirmação", 
    JOptionPane.YES_NO_OPTION);
if (confirm != JOptionPane.YES_OPTION) {
    return;
}

// Excluindo o serviço do banco
ServicoDAO servicoDAO = new ServicoDAO();
boolean sucesso = false;

sucesso = servicoDAO.excluirServico(id);

if (sucesso) {
    JOptionPane.showMessageDialog(this, "Serviço " + nomeServico + " excluído com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
    preencherTabela(); // Atualiza a tabela de serviços
} else {
    JOptionPane.showMessageDialog(this, "Erro ao excluir o serviço.", "Erro", JOptionPane.ERROR_MESSAGE);
}
    }//GEN-LAST:event_excluirServicoButtonActionPerformed


