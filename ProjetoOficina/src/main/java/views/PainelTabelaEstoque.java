    private void excluirProdutoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirProdutoButtonActionPerformed
     int selectedRow = estoqueTable.getSelectedRow();
        if (selectedRow != -1) {
            int clienteId = (int) estoqueTable.getValueAt(selectedRow, 0);
            EstoqueDAO dao = new EstoqueDAO();
            boolean sucesso = dao.excluirProduto(clienteId); // Chama o método modificado
            if (sucesso) {
                preencherTabela(); // Atualiza a tabela após a exclusão
                JOptionPane.showMessageDialog(this, "Produto excluído com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir produto.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um Produto para excluir.");
        }
        preencherTabela();
    }//GEN-LAST:event_excluirProdutoButtonActionPerformed

    private void editarProdutoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarProdutoButtonActionPerformed
    // Recuperar a linha selecionada na tabela
int selectedRow = estoqueTable.getSelectedRow();
if (selectedRow != -1) {
    int idProduto = (int) estoqueTable.getValueAt(selectedRow, 0); // ID do produto
    String nomeProduto = (String) estoqueTable.getValueAt(selectedRow, 1); // Nome do produto
    int quantidadeProduto = (int) estoqueTable.getValueAt(selectedRow, 2); // Quantidade (verifique se é Integer)
    double precoProduto = (double) estoqueTable.getValueAt(selectedRow, 3); // Preço (verifique se é Double)
    
    // Agora você pode editar os dados
    String novoNome = JOptionPane.showInputDialog(this, "Editar nome:", nomeProduto);
    String novaQuantidade = JOptionPane.showInputDialog(this, "Editar quantidade:", quantidadeProduto);
    String novoPreco = JOptionPane.showInputDialog(this, "Editar preço:", precoProduto);
    
    // A seguir, você deve garantir que os dados recuperados sejam corretamente convertidos para os tipos esperados.
    if (novoNome != null && novaQuantidade != null && novoPreco != null) {
        try {
            int quantidade = Integer.parseInt(novaQuantidade); // Converte a quantidade para Integer
            double preco = Double.parseDouble(novoPreco); // Converte o preço para Double
            
            Estoque produtoEditado = new Estoque(idProduto, novoNome, preco, quantidade);
            EstoqueDAO dao = new EstoqueDAO();
            boolean sucesso = dao.editarProduto(produtoEditado);
            if (sucesso) {
                preencherTabela();
                JOptionPane.showMessageDialog(this, "Estoque atualizado com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao atualizar o estoque.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Erro ao converter quantidade ou preço.");
        }
    }
} else {
    JOptionPane.showMessageDialog(this, "Selecione um produto para editar.");
}


    }//GEN-LAST:event_editarProdutoButtonActionPerformed


