     public void preencherTabela() {
         DefaultTableModel dtm = (DefaultTableModel) estoqueTable.getModel();
        dtm.setRowCount(0);
        EstoqueDAO dao = new EstoqueDAO();
        
        for(Estoque f : dao.listarProdutos()) {
            dtm.addRow(new Object[] {
                f.getId(),
                f.getNomeProduto(),
                f.getQuantidade(),
                f.getPreco(),
            });
        }
    }
    

    
 

